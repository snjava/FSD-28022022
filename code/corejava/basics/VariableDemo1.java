/**
	This class is use to demo the use for varaible. 
	Here we are creating multiple type of varaible for representation
*/
public class VariableDemo1
{
	public static void main(String args[]) {

		byte a; // declaration of varaible 
		a = 10; // intitalization of varaible

		short b = 20;
		int c = 30;
		long d = 40L;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);

		float e = 12.23F;
		double f = 22.33;
		char g = 'A';
		boolean h = true;
	
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);		

	}
}